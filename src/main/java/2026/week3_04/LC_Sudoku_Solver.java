/**
 * LeetCode 37. Sudoku Solver
 *
 * 9x9 스도쿠 보드를 백트래킹(Backtracking)으로 푸는 문제.
 *
 * 스도쿠 규칙:
 *   1) 각 행(row)에는 1~9가 중복 없이 한 번씩만 등장
 *   2) 각 열(column)에는 1~9가 중복 없이 한 번씩만 등장
 *   3) 9개의 3x3 박스(box) 각각에도 1~9가 중복 없이 한 번씩만 등장
 *
 * 풀이 전략:
 *   - 빈 칸('.')을 왼쪽 위에서 오른쪽 아래로 순서대로 찾는다.
 *   - 빈 칸에 '1'~'9'를 하나씩 시도해보고, 규칙을 위반하지 않으면 채워 넣는다.
 *   - 채운 뒤 재귀적으로 다음 빈 칸을 푼다(DFS).
 *   - 끝까지 가서 실패하면 직전 선택을 되돌린다(백트래킹).
 *
 * 시간 복잡도:
 *   - 최악의 경우 O(9^m), m은 빈 칸의 개수 (실제로는 가지치기로 훨씬 빠름)
 * 공간 복잡도:
 *   - O(m) (재귀 호출 스택 깊이)
 */
class Main {
    public static void main(String[] args) {
        // 입력 보드: '.'은 빈 칸을 의미한다.
        char[][] board = {
            {'5','3','.','.','7','.','.','.','.'},
            {'6','.','.','1','9','5','.','.','.'},
            {'.','9','8','.','.','.','.','6','.'},
            {'8','.','.','.','6','.','.','.','3'},
            {'4','.','.','8','.','3','.','.','1'},
            {'7','.','.','.','2','.','.','.','6'},
            {'.','6','.','.','.','.','2','8','.'},
            {'.','.','.','4','1','9','.','.','5'},
            {'.','.','.','.','8','.','.','7','9'}
        };

        solveSudoku(board); // 보드를 in-place로 채워 넣는다.
        printBoard(board);  // 결과 출력
    }

    /**
     * 스도쿠 보드를 백트래킹으로 채운다.
     *
     * @param board 9x9 char 배열. '.'이 빈 칸, '1'~'9'가 채워진 숫자.
     * @return 보드를 모두 유효하게 채울 수 있으면 true, 불가능하면 false.
     *
     * 동작 원리:
     *   1) 가장 처음 나오는 빈 칸을 찾는다.
     *   2) 그 칸에 '1'~'9'를 차례로 시도한다.
     *   3) isValid() 검사를 통과하면 그 숫자를 임시로 넣고, 재귀적으로 다음 빈 칸을 푼다.
     *   4) 재귀가 true를 반환하면 정답이 완성된 것이므로 그대로 true를 전파.
     *   5) 재귀가 false면 잘못 선택한 것이므로 '.'으로 되돌리고 다음 숫자를 시도(백트래킹).
     *   6) 1~9 모두 실패하면 이전 단계로 false를 반환해 다른 선택을 시도하게 한다.
     *   7) 빈 칸이 더 이상 없다면(모든 칸이 채워졌다면) true를 반환해 종료.
     */
    static boolean solveSudoku(char[][] board) {
        // 보드를 행 우선(row-major) 순서로 훑으며 빈 칸을 찾는다.
        for (int i = 0; i < 9; i++) {
            for (int j = 0; j < 9; j++) {
                // 이미 채워진 칸은 건너뛴다.
                if (board[i][j] != '.') continue;

                // '1'부터 '9'까지 후보 숫자를 시도한다. (char 비교이므로 '1' <= k <= '9')
                for (char k = '1' ; k <= '9' ; k++) {
                    // 행/열/3x3 박스 규칙을 모두 만족하는지 확인.
                    if (isValid(board, i, j, k)) {
                        board[i][j] = k; // 후보를 잠정적으로 배치

                        // 다음 빈 칸을 재귀적으로 푼다.
                        // 성공하면 정답을 찾은 것이므로 즉시 true 반환.
                        if (solveSudoku(board)) return true;

                        // 실패했다면 잘못된 선택이므로 원래대로 되돌린다(백트래킹).
                        board[i][j] = '.';
                    }
                }
                // 1~9 어떤 숫자도 들어갈 수 없다면, 이 분기는 실패.
                // 호출자에게 false를 알려서 직전 선택을 되돌리도록 한다.
                return false;
            }
        }
        // 빈 칸을 단 하나도 만나지 않고 루프가 끝났다는 것 = 보드가 모두 채워졌다는 것.
        return true;
    }

    /**
     * (i, j) 위치에 숫자 k를 두어도 스도쿠 규칙을 위반하지 않는지 검사한다.
     *
     * @param board 현재 보드 상태
     * @param i     검사할 칸의 행 인덱스 (0~8)
     * @param j     검사할 칸의 열 인덱스 (0~8)
     * @param k     배치하려는 숫자 ('1'~'9')
     * @return 행/열/3x3 박스 어디에도 k가 중복되지 않으면 true.
     *
     * 한 번의 루프(a: 0~8)로 세 가지 검사를 동시에 수행하는 것이 핵심:
     *   - board[i][a] : i행 전체를 훑으며 같은 숫자가 있는지
     *   - board[a][j] : j열 전체를 훑으며 같은 숫자가 있는지
     *   - board[boxRow + a/3][boxCol + a%3] : (i, j)가 속한 3x3 박스 9칸을 훑음
     */
    static boolean isValid(char[][] board, int i, int j, char k ) {
        // (i, j)가 속한 3x3 박스의 좌상단(top-left) 좌표를 계산.
        // 예: i=4, j=7 → boxRow=3, boxCol=6 (즉, 가운데-오른쪽 박스)
        // 정수 나눗셈으로 0/3/6 중 하나의 값이 나오게 정렬한다.
        int boxRow = (i / 3) * 3;
        int boxCol = (j / 3) * 3;

        // a를 0~8까지 돌리면서 행/열/박스 9칸을 한 번에 검사한다.
        for (int a = 0; a < 9; a++) {
            // 같은 행에 k가 이미 존재하면 위반.
            if (board[i][a] == k) return false;
            // 같은 열에 k가 이미 존재하면 위반.
            if (board[a][j] == k) return false;
            // 같은 3x3 박스 안에 k가 이미 존재하면 위반.
            // a/3 (0,0,0,1,1,1,2,2,2), a%3 (0,1,2,0,1,2,0,1,2)로
            // 박스 내부의 9칸을 (행, 열)로 풀어서 순회한다.
            if (board[boxRow + a / 3][boxCol + a % 3] == k) return false;
        }
        return true;
    }

    /**
     * 스도쿠 보드를 사람이 보기 좋은 형태로 출력한다.
     * 3x3 박스 경계는 '|'와 '------+-------+------' 구분선으로 시각화한다.
     *
     * 출력 예시:
     *   5 3 4 | 6 7 8 | 9 1 2
     *   6 7 2 | 1 9 5 | 3 4 8
     *   1 9 8 | 3 4 2 | 5 6 7
     *   ------+-------+------
     *   ...
     */
    static void printBoard(char[][] board) {
        for (int i = 0; i < 9; i++) {
            StringBuilder sb = new StringBuilder();
            for (int j = 0; j < 9; j++) {
                sb.append(board[i][j]);

                // 3번째, 6번째 열 뒤에는 박스 경계 '|'를 넣는다.
                if ( j == 2 || j == 5 ) sb.append("|");
                // 그 외에는 마지막 열(j=8)을 제외하고 가독성을 위해 공백을 넣는다.
                else if ( j < 8) sb.append(" ");
            }
            System.out.println(sb.toString());
            // 3번째, 6번째 행 아래에는 가로 박스 경계선을 그린다.
            if (i == 2 || i == 5) System.out.println("------+-------+------");
        }
    }
}
